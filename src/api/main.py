@router.get(
    "/health",
    response_model=HealthResponse,
    tags=["ops"],
    summary="Service health probe",
)
async def health(request: Request) -> HealthResponse:
    """Live connectivity and readiness probe.

    Performs a real ``describe_index_stats`` call against the configured
    Pinecone index (not just a ping) and checks whether ``ANTHROPIC_API_KEY``
    is present in the environment.  The overall ``status`` field summarises
    both checks:

    | status | meaning |
    |--------|---------|
    | `healthy` | Pinecone reachable **and** Anthropic key present |
    | `degraded` | Exactly one of the two checks passed |
    | `unhealthy` | Both checks failed |

    Also returns ``index_vector_count`` (useful for confirming the ingestion
    pipeline has run) and ``uptime_seconds`` since process start.

    This endpoint is **not rate-limited** and adds no auth requirement so it
    can be polled freely by load-balancer health checks and uptime monitors.
    """
    pinecone_ok = False
    vector_count = 0
    try:
        from pinecone import Pinecone

        _pc = Pinecone(api_key=settings.pinecone_api_key)
        _idx = _pc.Index(settings.pinecone_index_name)
        _stats = await asyncio.to_thread(_idx.describe_index_stats)
        vector_count = _stats.total_vector_count
        pinecone_ok = True
    except Exception as exc:
        logger.warning("Health check: Pinecone probe failed: %s", exc)

    anthropic_ok = bool(settings.anthropic_api_key)

    if pinecone_ok and anthropic_ok:
        status = "healthy"
    elif pinecone_ok or anthropic_ok:
        status = "degraded"
    else:
        status = "unhealthy"

    uptime = time.monotonic() - getattr(request.app.state, "start_time", time.monotonic())

    return HealthResponse(
        status=status,
        pinecone_connected=pinecone_ok,
        anthropic_connected=anthropic_ok,
        index_vector_count=vector_count,
        uptime_seconds=round(uptime, 1),
        version=_VERSION,
    )


