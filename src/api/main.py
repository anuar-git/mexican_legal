async def _check_rate_limit(request: Request) -> None:
    """Dependency: raise 429 if the client IP has exceeded the rate limit."""
    ip = request.client.host if request.client else "unknown"
    if not _rate_limiter.is_allowed(ip):
        QUERY_COUNTER.labels(status="rate_limited").inc()
        retry_after = int(_rate_limiter.time_until_next(ip)) + 1
        raise HTTPException(
            status_code=429,
            detail=ErrorResponse(
                error="Rate limit exceeded",
                detail=f"Retry after {retry_after}s ({_RATE_LIMIT_REQUESTS} req/{int(_RATE_LIMIT_WINDOW_S)}s per IP)",
                request_id=getattr(request.state, "request_id", "unknown"),
            ).model_dump(),
            headers={"Retry-After": str(retry_after)},
        )


async def _verify_admin_key(
    x_admin_key: str = Header(..., alias="X-Admin-Key"),
) -> None:
    """Dependency: validate the X-Admin-Key header for admin-only endpoints."""
    if not _ADMIN_API_KEY:
        raise HTTPException(
            status_code=503,
            detail="Admin access is not configured on this instance (ADMIN_API_KEY not set)",
        )
    if x_admin_key != _ADMIN_API_KEY:
        raise HTTPException(status_code=403, detail="Invalid admin key")


