@router.post(
    "/query/stream",
    tags=["rag"],
    summary="RAG query (SSE streaming)",
    responses={
        429: {"model": ErrorResponse, "description": "Rate limit exceeded"},
        500: {"description": "Error event sent in stream"},
    },
    response_class=EventSourceResponse,
)
async def query_stream_endpoint(
    req: QueryRequest,
    request: Request,
    _: None = Depends(_check_rate_limit),
) -> EventSourceResponse:
    """Stream the full RAG pipeline as a sequence of Server-Sent Events.

    The stream delivers **three event types** in order:

    ``event: retrieval``
        Emitted once, immediately after the retrieval stage completes
        (expand → search → rerank).  Data is a JSON-serialised
        ``RetrievalMetadata`` object.  Clients can display chunk count,
        expanded queries, and latency before the first token arrives.

    ``event: token``
        Emitted for every text token from Claude Haiku.  Data is
        ``{"token": "<text>"}`` so clients can incrementally render the
        answer as it streams.

    ``event: complete``
        Emitted once after the last token.  Data is a JSON-serialised
        ``QueryResponse`` — identical to the response from ``POST /v1/query``
        including grounded citations and hallucination flags.  Citations are
        extracted post-hoc from the accumulated answer text; no second LLM
        call is made.

    ``event: error``
        Emitted if an unhandled exception occurs at any stage.  Data is
        ``{"error": "<message>", "request_id": "<id>"}``.  The connection is
        then closed.

    **SSE client example** (JavaScript)::

        const source = new EventSource(...)  // not supported for POST — use fetch + ReadableStream
        // or with fetch:
        const resp = await fetch('/v1/query/stream', {method:'POST', body: JSON.stringify(req)})
        const reader = resp.body.getReader()
    """
    request_id = getattr(request.state, "request_id", "unknown")
    t0 = time.monotonic()
    ACTIVE_REQUESTS.inc()

    async def _event_generator():
        try:
            # ── Event 1: retrieval ──────────────────────────────────────────
            # Run retrieval in its own step so we can emit metadata before
            # the (slow) generation stage starts.
            retrieval = await _pipeline.retrieve(req.question, req.filters, req.top_k)
            retrieval_meta = RetrievalMetadata.from_retrieval_result(retrieval)
            yield {
                "event": "retrieval",
                "data": retrieval_meta.model_dump_json(),
            }

            # ── Event 2: token (one per Claude output token) ────────────────
            # Pass the pre-computed RetrievalResult to skip a second retrieval
            # call inside query_stream().
            gen_t0 = time.monotonic()
            full_text_parts: list[str] = []

            async for token in _pipeline.query_stream(
                req.question, req.filters, retrieval=retrieval
            ):
                full_text_parts.append(token)
                yield {
                    "event": "token",
                    "data": json.dumps({"token": token}),
                }

            gen_ms = (time.monotonic() - gen_t0) * 1000
            full_text = "".join(full_text_parts)

            # ── Post-stream citation extraction (no second LLM call) ────────
            # Reuse the same regex + chunk cross-reference logic the blocking
            # endpoint uses, but applied to the accumulated streamed text.
            article_numbers = extract_article_numbers(full_text)
            citations = build_citations(article_numbers, retrieval.chunks)
            grounded, flagged = split_citations(citations)

            total_ms = (time.monotonic() - t0) * 1000

            # ── Event 3: complete ───────────────────────────────────────────
            complete = QueryResponse(
                answer=full_text,
                citations=grounded,
                retrieval=retrieval_meta,
                generation_time_ms=round(gen_ms, 2),
                total_time_ms=round(total_ms, 2),
                model=ACTIVE_MODEL,
                prompt_version=ACTIVE_PROMPT_VERSION,
                hallucination_flags=flagged,
            )
            yield {
                "event": "complete",
                "data": complete.model_dump_json(),
            }

            _metrics.record_request(total_ms, retrieval_meta.avg_similarity_score)
            if _request_logger is not None:
                await asyncio.to_thread(
                    _request_logger.log, request_id, req.question, complete, 200
                )

            # ── Prometheus ──────────────────────────────────────────────────
            QUERY_COUNTER.labels(status="success").inc()
            QUERY_LATENCY.labels(endpoint="stream").observe(total_ms / 1000)
            RETRIEVAL_LATENCY.observe(retrieval.retrieval_time_ms / 1000)
            GENERATION_LATENCY.observe(gen_ms / 1000)
            if flagged:
                HALLUCINATION_COUNTER.inc(len(flagged))

            logger.info(
                "stream complete",
                extra={
                    "request_id": request_id,
                    "total_ms": round(total_ms, 1),
                    "retrieval_ms": retrieval.retrieval_time_ms,
                    "generation_ms": round(gen_ms, 1),
                    "tokens": len(full_text_parts),
                    "citations": len(grounded),
                    "hallucination_flags": len(flagged),
                },
            )

        except Exception as exc:
            QUERY_COUNTER.labels(status="error").inc()
            _metrics.record_error()
            logger.exception(
                "Streaming pipeline error",
                extra={"request_id": request_id},
            )
            yield {
                "event": "error",
                "data": json.dumps({"error": str(exc), "request_id": request_id}),
            }
        finally:
            ACTIVE_REQUESTS.dec()

    return EventSourceResponse(_event_generator())


