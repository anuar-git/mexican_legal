@router.post(
    "/query",
    response_model=QueryResponse,
    responses={
        422: {"model": ErrorResponse, "description": "Validation error"},
        429: {"model": ErrorResponse, "description": "Rate limit exceeded"},
        500: {"model": ErrorResponse, "description": "Pipeline error"},
    },
    tags=["rag"],
    summary="RAG query (blocking)",
)
async def query_endpoint(
    req: QueryRequest,
    request: Request,
    _: None = Depends(_check_rate_limit),
) -> QueryResponse:
    """Run the full RAG pipeline and return a structured answer.

    Executes four stages synchronously (offloaded to a thread pool so the
    event loop stays unblocked):

    1. **Query expansion** — Claude Haiku generates 3 alternative phrasings of
       the question to improve recall.
    2. **Dense search** — Pinecone returns up to `top_k × 4` candidate chunks
       per variant; results are merged and deduplicated by chunk ID.
    3. **Cross-encoder reranking** — MiniLM-L6 reranks candidates to `top_k`
       highest-relevance chunks.
    4. **Generation** — Claude Haiku answers using the reranked chunks as
       context, then cited article numbers are extracted and cross-referenced
       against the retrieved chunks (hallucination detection).

    **Response** — `QueryResponse` containing the natural-language answer,
    grounded citations with cosine-similarity confidence scores, retrieval
    diagnostics (timing, expanded queries, chunk counts), and any article
    numbers cited by the model that were absent from retrieved context
    (`hallucination_flags`).

    **Errors**

    | Status | Condition |
    |--------|-----------|
    | 422 | Request body fails validation (question too short/long, top_k out of range) |
    | 429 | Client IP has exceeded the per-IP rate limit — check `Retry-After` header |
    | 500 | Pinecone or Anthropic API failure during the pipeline run |

    Set `stream: true` in the request body as a hint to switch to
    `POST /v1/query/stream` for token-by-token streaming.
    """
    request_id = getattr(request.state, "request_id", "unknown")
    ACTIVE_REQUESTS.inc()
    t0 = time.monotonic()

    try:
        result = await _pipeline.query(req.question, req.filters, req.top_k)
        total_ms = (time.monotonic() - t0) * 1000

        # Record Prometheus metrics
        QUERY_COUNTER.labels(status="success").inc()
        QUERY_LATENCY.labels(endpoint="query").observe(total_ms / 1000)
        RETRIEVAL_LATENCY.observe(result.retrieval.retrieval_time_ms / 1000)
        GENERATION_LATENCY.observe(result.generation_time_ms / 1000)
        n_flags = sum(1 for c in result.citations if c.hallucination_flag)
        if n_flags:
            HALLUCINATION_COUNTER.inc(n_flags)

        response = QueryResponse.from_generation_result(result, total_ms)  # type: ignore[arg-type]

        _metrics.record_request(total_ms, response.retrieval.avg_similarity_score)
        if _request_logger is not None:
            await asyncio.to_thread(
                _request_logger.log, request_id, req.question, response, 200
            )

        logger.info(
            "query complete",
            extra={
                "request_id": request_id,
                "total_ms": round(total_ms, 1),
                "retrieval_ms": result.retrieval.retrieval_time_ms,
                "generation_ms": result.generation_time_ms,
                "citations": len(response.citations),
                "hallucination_flags": len(response.hallucination_flags),
            },
        )
        return response

    except HTTPException:
        raise
    except Exception as exc:
        total_s = time.monotonic() - t0
        QUERY_COUNTER.labels(status="error").inc()
        QUERY_LATENCY.labels(endpoint="query").observe(total_s)
        _metrics.record_error()
        logger.exception(
            "Query pipeline error",
            extra={"request_id": request_id},
        )
        raise HTTPException(
            status_code=500,
            detail=ErrorResponse(
                error="Pipeline error",
                detail=str(exc),
                request_id=request_id,
            ).model_dump(),
        ) from exc
    finally:
        ACTIVE_REQUESTS.dec()


