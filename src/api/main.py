@asynccontextmanager
async def lifespan(app: FastAPI):
    """Probe external services on startup; log clean shutdown."""
    global _request_logger

    app.state.start_time = time.monotonic()
    _request_logger = RequestLogger()

    logger.info("Starting Legal Intelligence Engine v%s", _VERSION)

    # ── Pinecone probe ──────────────────────────────────────────────────────
    vector_count = 0
    try:
        from pinecone import Pinecone

        _pc = Pinecone(api_key=settings.pinecone_api_key)
        _idx = _pc.Index(settings.pinecone_index_name)
        _stats = await asyncio.to_thread(_idx.describe_index_stats)
        vector_count = _stats.total_vector_count
        logger.info(
            "Pinecone connected",
            extra={"index": settings.pinecone_index_name, "vectors": vector_count},
        )
    except Exception as exc:
        logger.warning("Pinecone probe failed: %s", exc)

    # ── Anthropic probe (key presence only; avoids API cost at startup) ─────
    anthropic_ok = bool(settings.anthropic_api_key)
    if anthropic_ok:
        logger.info("Anthropic API key present")
    else:
        logger.warning("ANTHROPIC_API_KEY not set — generation will fail at query time")

    yield  # ── application is running ──────────────────────────────────────

    if _request_logger is not None:
        _request_logger.close()
    logger.info("Legal Intelligence Engine shutting down")


