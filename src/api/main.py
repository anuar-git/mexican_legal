def _run_evaluation_bg(req: EvalRequest, job_id: str) -> None:
    """Background task that runs the full RAGAS + citation evaluation.

    Executed in a thread-pool worker so it does not block the event loop.
    Results are saved to ``results/eval_{run_id}.json``.

    Args:
        req:    The EvalRequest from the API caller.
        job_id: Short hex ID to correlate background log lines.
    """
    from src.evaluation.evaluator import Evaluator  # lazy import — heavy deps

    try:
        logger.info(
            "Eval job started",
            extra={"job_id": job_id, "test_set": req.test_set_path},
        )
        evaluator = Evaluator(test_set_path=req.test_set_path)

        run = evaluator.run(
            categories=[req.category] if req.category else None,
        )

        # If a single question_id was requested, filter post-run
        if req.question_id:
            run.results = [r for r in run.results if r.question_id == req.question_id]
            run.recalculate_aggregates()

        out_path = f"results/eval_{run.run_id}.json"
        run.save(out_path)

        summary = run.score_summary()
        logger.info(
            "Eval job complete",
            extra={"job_id": job_id, "run_id": run.run_id, "output": out_path, **summary},
        )

    except FileNotFoundError as exc:
        logger.error(
            "Eval job failed — test set not found",
            extra={"job_id": job_id, "error": str(exc)},
        )
    except Exception as exc:
        logger.exception(
            "Eval job failed",
            extra={"job_id": job_id, "error": str(exc)},
        )


@router.post(
    "/evaluate",
    status_code=202,
    tags=["ops"],
    summary="Trigger evaluation run (admin)",
    dependencies=[Depends(_verify_admin_key)],
    responses={
        202: {"description": "Evaluation job accepted and running in background"},
        403: {"model": ErrorResponse, "description": "Invalid admin key"},
        503: {"model": ErrorResponse, "description": "Admin access not configured"},
    },
)
async def evaluate(
    req: EvalRequest,
    background_tasks: BackgroundTasks,
    request: Request,
) -> dict:
    """Trigger a RAGAS + citation-accuracy evaluation run (admin only).

    Returns **202 Accepted** immediately; the full evaluation pipeline runs in
    a background thread pool worker and does **not** block the event loop or
    hold an HTTP connection open.

    **What runs in the background**:

    1. `Evaluator` loads the golden test set from `test_set_path`.
    2. For each question (optionally filtered by `category` or `question_id`),
       the full RAG pipeline is executed and scored with RAGAS metrics
       (faithfulness, answer relevance, context precision/recall) plus a custom
       citation-accuracy metric.
    3. Aggregate scores and per-question results are written to
       `results/eval_{run_id}.json`.

    **Tracking progress** — all log lines from the background job carry a
    `job_id` field matching the value returned in the 202 response.  Filter
    server logs by `job_id` to follow a specific run.

    **Auth** — the `X-Admin-Key` header must match the `ADMIN_API_KEY`
    environment variable.  Returns 403 on mismatch, 503 if `ADMIN_API_KEY`
    is not configured on this instance.

    | Status | Condition |
    |--------|-----------|
    | 202 | Job accepted and running in background |
    | 403 | `X-Admin-Key` header value does not match `ADMIN_API_KEY` |
    | 503 | `ADMIN_API_KEY` is not set on this instance |
    """
    job_id = uuid.uuid4().hex[:8]
    # BackgroundTasks runs in the thread pool after the response is sent.
    # Evaluator.run() calls asyncio.run() internally, which is safe inside
    # a thread-pool worker (separate event loop per thread).
    background_tasks.add_task(_run_evaluation_bg, req, job_id)

    return {
        "job_id": job_id,
        "status": "started",
        "message": (
            f"Evaluation job {job_id} is running in the background. "
            f"Results will be saved to results/eval_{{run_id}}.json. "
            f"Monitor progress via server logs (filter: job_id={job_id})."
        ),
        "request_id": getattr(request.state, "request_id", "unknown"),
    }


